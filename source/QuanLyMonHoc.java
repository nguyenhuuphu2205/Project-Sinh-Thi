    private ArrayList<MonHoc> dsMonHoc = NganHangCauHoi.dsMonHoc();

    private void setData(int selectedIndexMonHoc) {
        if (selectedIndexMonHoc >= 0) {
            this.jText_tenMonHoc.setText(dsMonHoc.get(selectedIndexMonHoc).gettenMonHoc());
            this.jTex_mahocphan.setText(dsMonHoc.get(selectedIndexMonHoc).getmaHocPhan());
            this.jText_gioi_thieu.setText(dsMonHoc.get(selectedIndexMonHoc).getgioiThieu());
            this.jS_so_chuong.setValue((int) dsMonHoc.get(selectedIndexMonHoc).soLuongChuong());

        } else {
            this.jText_tenMonHoc.setText("");
            this.jTex_mahocphan.setText("");
            this.jText_gioi_thieu.setText("");
            this.jS_so_chuong.setValue(0);
        }
    }

    private void setData_list_monHoc() {
        this.jLi_dsMonHoc.setListData(NganHangCauHoi.toArrayString_MonHoc(dsMonHoc));
    }

    private MonHoc getData() {
        MonHoc monhocSelected = new MonHoc();
        monhocSelected.settenMonHoc(this.jText_tenMonHoc.getText());
        monhocSelected.setmaHocPhan(this.jTex_mahocphan.getText());
        monhocSelected.setgioiThieu(this.jText_gioi_thieu.getText());
        monhocSelected.setsoLuongChuong((int) this.jS_so_chuong.getValue());
        return monhocSelected;
    }

