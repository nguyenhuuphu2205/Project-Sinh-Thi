    private void jB_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_themActionPerformed
        // TODO add your handling code here:
        MonHoc monhocthem = new MonHoc();

        monhocthem = this.getData();
        dsMonHoc.add(monhocthem);
        this.jLi_dsMonHoc.setListData(NganHangCauHoi.toArrayString_MonHoc(dsMonHoc));
        this.setData(dsMonHoc.size() - 1);
    }//GEN-LAST:event_jB_themActionPerformed

