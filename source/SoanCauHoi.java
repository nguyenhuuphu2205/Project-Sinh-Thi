    private void setData(int selectedIndexCauHoi) {
        if (selectedIndexCauHoi >= 0) {
            CauHoi ch = this.dsCauHoi.get(selectedIndexCauHoi);
            this.jTextArea1.setText(ch.gettenCauHoi());
            String[] dsDA = new String[ch.getsoLuongDapAn()];
            for (int i = 0; i < dsDA.length; i++) {
                if (ch instanceof CauHoiTN) {
                    dsDA[i] = ((CauHoiTN) ch).getdsDapAn().get(i);
                } else {
                    dsDA[i] = ((CauHoiTL) ch).getDapAn();
                }
            }
            this.jList2.setListData(dsDA);

        } else {
            this.jTextArea1.setText("");
            String[] a = {};
            this.jList2.setListData(a);
        }

    }

    private void setData_ds_CauHoi(ArrayList<CauHoi> dsCauHoi) {
        String[] dstenCauHoi = new String[dsCauHoi.size()];
        for (int i = 0; i < dstenCauHoi.length; i++) {
            dstenCauHoi[i] = dsCauHoi.get(i).gettenCauHoi();
        }
        this.jList1.setListData(dstenCauHoi);
    }

    public SoanCauHoi(int selectedIndex) {
        super("Soạn câu hỏi");
        this.selectedIndex = selectedIndex;
        this.monhoc = dsMonHoc.get(selectedIndex);
        this.setVisible(true);
        dsCauHoi = NganHangCauHoi.dsCauHoiMon(monhoc);
        String[] dstenCauHoi = NganHangCauHoi.toArrayString_dsCauHoiMon(monhoc);

        initComponents();
        this.jList1.setListData(dstenCauHoi);
        int sochuong = monhoc.soLuongChuong();
        for (int i = 0; i < sochuong; i++) {
            this.jCo_Chuong.addItem(monhoc.getdsChuong().get(i).gettenChuong());
        }
        this.setData(0);

    }

