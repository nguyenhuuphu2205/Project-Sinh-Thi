    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        String t = this.jList1.getSelectedValue();
        this.jTextArea1.setText(t);
        if (dsCauHoi.get(this.jList1.getSelectedIndex()) instanceof CauHoiTL) {
            this.jRadioButton2.setSelected(true);
            String[] a = new String[1];
            a[0] = ((CauHoiTL) dsCauHoi.get(this.jList1.getSelectedIndex())).getDapAn();
            this.jList2.setListData(a);
            this.jCo_Chuong.setSelectedItem(((CauHoiTL) dsCauHoi.get(this.jList1.getSelectedIndex())).getchuong());
            this.jCo_DoKho.setSelectedItem(((CauHoiTL) dsCauHoi.get(this.jList1.getSelectedIndex())).getdoKho());
        }
        if (dsCauHoi.get(this.jList1.getSelectedIndex()) instanceof CauHoiTN) {
            this.jRadioButton1.setSelected(true);
            String[] a = new String[dsCauHoi.get(this.jList1.getSelectedIndex()).getsoLuongDapAn()];
            for (int i = 0; i < dsCauHoi.get(this.jList1.getSelectedIndex()).getsoLuongDapAn(); i++) {
                a[i] = ((CauHoiTN) dsCauHoi.get(this.jList1.getSelectedIndex())).getdsDapAn().get(i);
            }
            this.jList2.setListData(a);
            this.jCo_Chuong.setSelectedItem(((CauHoiTN) dsCauHoi.get(this.jList1.getSelectedIndex())).getchuong());
            this.jCo_DoKho.setSelectedItem(((CauHoiTN) dsCauHoi.get(this.jList1.getSelectedIndex())).getdoKho());
        }

    }//GEN-LAST:event_jList1MouseClicked

