    private void jButton_them_cauHoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_them_cauHoiActionPerformed
        // TODO add your handling code here:
        if (this.jRadioButton1.isSelected()) {
            CauHoiTN ch = new CauHoiTN();
            ch.settenCauHoi(this.jTextArea1.getText());
            ch.setchuong(this.jCo_Chuong.getSelectedItem().toString());
            ch.setdoKho(this.jCo_DoKho.getSelectedItem().toString());
            ch.setmonHoc(this.monhoc.gettenMonHoc());
            this.dsCauHoi.add(ch);
            this.setData_ds_CauHoi(dsCauHoi);
            this.setData(dsCauHoi.size() - 1);
            this.jList1.setSelectedIndex(dsCauHoi.size() - 1);

        } else {
            CauHoiTL ch = new CauHoiTL();
            ch.settenCauHoi(this.jTextArea1.getText());
            ch.setchuong(this.jCo_Chuong.getSelectedItem().toString());
            ch.setdapAn("Khong co dap an");
            ch.setmonHoc(this.monhoc.gettenMonHoc());
            ch.setdoKho(this.jCo_DoKho.getSelectedItem().toString());
            this.dsCauHoi.add(ch);
            this.setData_ds_CauHoi(dsCauHoi);
            this.setData(dsCauHoi.size() - 1);
            this.jList1.setSelectedIndex(dsCauHoi.size() - 1);
        }
    }//GEN-LAST:event_jButton_them_cauHoiActionPerformed

